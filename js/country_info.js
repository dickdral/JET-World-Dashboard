function get_country_table( country_id, subject, sort_direction )
{
    var table = [];
    var rank  = 0;
    var rank_country = null;
    var display_class;
    
    // make a copy of all country data ( no regions )
    var table_data = country_data.slice(idx_country_base);

    // sort the table according to subject
    if ( subject == 'agriculture')
    {   sort_idx = idx_land_area;    }
    else if ( subject == 'energy')
    {   sort_idx = idx_oil_cons_per_inh;     }
    else if ( subject == 'health')
    {   csort_idx = idx_doctors_per_10t_inh; }
    else if ( subject == 'mobility')
    {   sort_idx = idx_cars_per_1000_inh;    }
    else if ( subject == 'population')
    {   sort_idx = idx_population_density;   }
    else
    {   sort_idx = idx_land_area; }
    sort_idx = subject_data[subject].idx;
    
    // sort descending
    table_data.sort( function(a,b) { return( b[sort_idx] - a[sort_idx] ) } );
    
    for ( i in table_data)
    {
        var data = table_data[i];
        if ( parseInt(data[idx_country_id]) >= 100 && data[sort_idx] != undefined && data[sort_idx] != null ) 
        { 
            var rank = rank + 1;
            if ( data[idx_country_id] == country_id )
            { rank_country = rank; }
                    
            country_rec = { rank         : rank
                   , country_code : data[idx_country_code]
                   , country_name : data[idx_country_name]
                   , land_area    : data[idx_land_area]
                   , population   : data[idx_population]
                   , display_class: "dc_hide"};

            if ( subject == 'agriculture')
            {   country_rec.perc_farmland = data[idx_area_perc_farmland];
            }
            else if ( subject == 'energy')
            {   country_rec.oil_cons_per_inh = data[idx_oil_cons_per_inh];
            }
            else if ( subject == 'health')
            {   country_rec.doctors_per_10t_inh = data[idx_doctors_per_10t_inh];
            }
            else if ( subject == 'mobility')
            {   country_rec.cars_per_1000_inh = data[idx_cars_per_1000_inh];
                country_rec.road_network_in_km  = data[idx_road_network_in_km];
            }
            else if ( subject == 'population')
            {   country_rec.population_density = data[idx_population_density];
                country_rec.population_growth  = data[idx_population_growth];
            }
          
            table.push( country_rec );
        }        
    }
    
    for ( i in table )
    {
        rank = table[i].rank;
        display_class = 'dc_hide';

        if ( Math.abs(rank - rank_country) == 3 )
        { display_class = "dc_grey"; }
            
        if ( rank <= 3 || Math.abs(rank - rank_country) <= 2 || rank >= table.length - 2 )
        { display_class = "dc_show"; }
        
        if ( rank == rank_country)
        { display_class = "dc_current"; }
        
        table[i].display_class= display_class;            
        
    }

    return(table);
}

