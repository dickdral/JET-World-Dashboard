compress_country_table = function( country_code)
{
    var rank = 1;
    var rank_country = -10;

    var table = $('#country_ranking_table');
    var numrows = $(table).find('tbody tr').length;
    
    // determine rank and country rank
    $(table).find('tbody tr').each(
        function()
        {             
            var display_class = 'dc_hide';

            // apply rank to country
            $(this).find('[headers^="table:_hdrCol0"]').html(rank);

            // determine rank of current country
            var code = $(this).find('[name="country_code"]').val();
            if ( code == country_code )
            { rank_country = rank;
              console.log('rank country='+rank_country);
              var tr_prev = set_dc_class( $(this).prev() ,'dc_show');
              tr_prev     = set_dc_class( $(tr_prev).prev() ,'dc_show');
              tr_prev     = set_dc_class( $(tr_prev).prev() ,'dc_grey');
            }
            
            // set grey if distance to current country = 3
            if ( Math.abs(rank - rank_country) == 3 )
            { display_class = "dc_grey"; }

            // show if : - rank in first or last three
            //           - rank within 2 of current country
            if ( rank <= 3 || Math.abs(rank - rank_country) <= 2 || rank >= numrows - 2 )
            { display_class = "dc_show"; }

            if ( rank == rank_country)
            { display_class = "dc_current"; }

            set_dc_class(this,display_class);
            
            rank++;
        });        
}

